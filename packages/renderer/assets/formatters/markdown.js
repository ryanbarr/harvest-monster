import { formatPrice } from "../../utils/formatPrice";
import { parsePrice } from "../../utils/parsePrice";

export default {
  key: "markdown",
  name: "Markdown",
  prefix: ({
    currentLeagueName,
    username,
    customNotes,
    exchangeRate,
    includeExchangeRate,
    willingToStream,
  }) => {
    let post = "```md\n";

    post += `WTS ${currentLeagueName}`;

    if (username && username.trim() !== "") {
      post += ` - IGN: ${username}`;
    }

    post += ` | [generated by](HarvestMonster)\n`;

    post += `==============================\n`;

    // Custom Notes
    if (customNotes.trim() !== "") {
      post += `/* ${customNotes} */\n`;
    }

    // Exchange Rate
    if (includeExchangeRate) {
      post += `[Exchange Rate: ](${exchangeRate}c to 1ex)\n`;
    }

    // Willing to Stream
    if (willingToStream) {
      post += `> Can stream if requested\n`;
    }

    post += `\n`;

    return post;
  },
  craft: (craft, { maxCraftLength }) => {
    let post = "";
    const quantityLengthAdjustment = `${craft.quantity}`.length - 1;
    let bufferLength =
      maxCraftLength - craft.name.length + 3 - quantityLengthAdjustment;

    const buffer = Array.from(Array(bufferLength), () => "").join(" ");

    if (craft.quantity === 0) {
      post += "~~";
    }

    post += `${craft.quantity}x | ${craft.name}${buffer} [${
      craft.level > 0 ? craft.level : "83"
    }] <${formatPrice(parsePrice(craft.displayPrice))}>`;

    if (craft.quantity === 0) {
      post += "~~";
    }

    post += "\n";

    return post;
  },
  suffix: () => "```",
};
